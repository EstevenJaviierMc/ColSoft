    private void BtnGrabar3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnGrabar3ActionPerformed
        // TODO add your handling code here:
        System.out.println(txtEstado.getSelectedItem().toString());
        
        if (txtCodigo.getText().equals("")) {
            cr.setVisible(true);
            
        } else {
            cr.setVisible(false);
        }
        
        if (txtNombre.getText().equals("")) {
            cr1.setVisible(true);
            return;
        } else {
            cr1.setVisible(false);
        }
        
        Facultad f1 = new Facultad(txtNombre.getName().trim(), txtEstado.getSelectedItem().toString());

        System.out.println(f1.toString());

        ConectarBD cx = new ConectarBD();
        ControladorFacultad fjdbc = new ControladorFacultad();
        try {
            cx.conectarme();
            fjdbc.setCon(cx.getCon());
            fjdbc.grabarFacultad(f1);

        } catch (SQLException ex) {
            System.out.println(ex.getMessage());
            if (ex.getErrorCode() == 1062) {
                System.out.println("La facultad que desea ingresar ya Existe!");
            };
        }
    }//GEN-LAST:event_BtnGrabar3ActionPerformed

